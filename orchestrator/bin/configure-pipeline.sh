#!/usr/bin/env bash
# Interactive worker assignment for a pipeline run. Writes orchestrator/workers.conf.
# Re-runnable; previous values are shown as defaults.

set -euo pipefail
SELF_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
source "$SELF_DIR/_lib.sh"
load_workers_conf

CLASSES=(copilot cursor cursor-mcp cursor-ask claude-cli shell)
PHYSICAL=(copilot cursor cursor-mcp cursor-ask claude-cli shell)

ask() {
  local prompt=$1 default=$2 reply
  printf '%s [%s]: ' "$prompt" "$default" >&2
  read -r reply
  echo "${reply:-$default}"
}

ask_choice() {
  local prompt=$1 default=$2; shift 2
  local options=("$@") opt reply
  printf '%s\n' "$prompt" >&2
  for i in "${!options[@]}"; do
    printf '  %d) %s%s\n' "$((i + 1))" "${options[$i]}" \
      "$([ "${options[$i]}" = "$default" ] && echo ' (default)')" >&2
  done
  printf 'choice [%s]: ' "$default" >&2
  read -r reply
  if [[ -z "$reply" ]]; then
    echo "$default"; return
  fi
  if [[ "$reply" =~ ^[0-9]+$ ]] && (( reply >= 1 && reply <= ${#options[@]} )); then
    echo "${options[$((reply - 1))]}"
  else
    echo "$reply"
  fi
}

echo "== Pipeline worker configuration ==" >&2
echo "Press Enter to keep current value. For MCP class, only cursor-mcp is valid." >&2
echo >&2

declare -A REMAP_NEW
for logical in "${CLASSES[@]}"; do
  key="REMAP_${logical//-/_}"
  current="${!key:-$logical}"
  if [[ "$logical" == "cursor-mcp" ]]; then
    # cursor-mcp can only be itself or shell (no other class has --approve-mcps)
    new=$(ask_choice "Worker for logical class: $logical" "$current" cursor-mcp shell)
  else
    new=$(ask_choice "Worker for logical class: $logical" "$current" "${PHYSICAL[@]}")
  fi
  REMAP_NEW[$logical]=$new
done

echo >&2
current_model="${CLAUDE_CLI_MODEL:-sonnet}"
new_model=$(ask_choice "claude-cli model" "$current_model" \
  haiku sonnet opus "haiku[1m]" "sonnet[1m]" "opus[1m]")

echo >&2
echo "About to write:" >&2
for logical in "${CLASSES[@]}"; do
  printf '  REMAP_%s=%s\n' "${logical//-/_}" "${REMAP_NEW[$logical]}" >&2
done
printf '  CLAUDE_CLI_MODEL=%s\n' "$new_model" >&2
echo >&2
confirm=$(ask "Write to $ORCH_WORKERS_CONF? [y/N]" "n")

if [[ "$confirm" != "y" && "$confirm" != "Y" ]]; then
  echo "aborted" >&2
  exit 1
fi

# Replace REMAP_* and CLAUDE_CLI_MODEL lines in place; preserve comments.
tmp=$(mktemp)
awk -v model="$new_model" '
  /^REMAP_/ { next }
  /^CLAUDE_CLI_MODEL=/ { next }
  { print }
' "$ORCH_WORKERS_CONF" > "$tmp"

{
  echo
  echo "# === Generated by configure-pipeline.sh $(ts) ==="
  for logical in "${CLASSES[@]}"; do
    printf 'REMAP_%s=%s\n' "${logical//-/_}" "${REMAP_NEW[$logical]}"
  done
  printf 'CLAUDE_CLI_MODEL=%s\n' "$new_model"
} >> "$tmp"

mv "$tmp" "$ORCH_WORKERS_CONF"
echo "wrote $ORCH_WORKERS_CONF" >&2
